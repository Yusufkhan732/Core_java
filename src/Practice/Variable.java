package Practice;

public class Variable {

	public static void main(String[] args) {

		System.out.println("Hello\bworld....");

		// initialization

		// variable identifiers initialization matlab value asgin karna
		int mynumber = 675;
		System.out.println(mynumber);

		// declaration

		int yournumber; // variable declaration
		yournumber = 90;
		System.out.println(yournumber);

		yournumber = 88;
		System.out.println(yournumber);

		// Boolean True / False declaration

		boolean isVegetarian = true;
		System.out.println(isVegetarian);

		// Decimal 1.0 declaration

		float fl = 5.0f;
		float myfloat = 5;
		System.out.println(myfloat);

		// Double declaration
		double mydouble = 4.66655d;
		System.out.println(mydouble);

		// String declaration double codes mein

		String st = "Rohit";
		System.out.println(st);

		// Character declaration

		char c = 'A';
		System.out.println(c);
	}
}
